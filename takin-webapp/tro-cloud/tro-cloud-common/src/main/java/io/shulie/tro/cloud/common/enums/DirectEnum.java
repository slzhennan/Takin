package io.shulie.tro.cloud.common.enums;

/**
 * @ClassName DirectEnum
 * @Description
 * @Author qianshui
 * @Date 2020/5/9 下午6:01
 */
public enum DirectEnum {

    FORWARD(0, "正向"),
    BACK(1, "反向");

    private Integer value;
    private String name;

    DirectEnum(Integer value, String name) {
        this.value = value;
        this.name = name;
    }

    public Integer getValue() {
        return value;
    }

    public void setValue(Integer value) {
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
