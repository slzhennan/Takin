package io.shulie.tro.web.auth.api.enums;

/**
 * @Author: fanxx
 * @Date: 2021/3/8 3:15 下午
 * @Description:
 */
public enum UserTypeEnum {
    ADMIN(0, "系统管理员"),
    NORMAL(1, "普通用户");

    private Integer code;

    private String type;

    UserTypeEnum(Integer code, String type) {
        this.code = code;
        this.type = type;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
