package com.pamirs.tro.entity.domain.entity.auth;

import java.util.List;

public class BaseRole {
    private Long id;

    private String name;

    private String alias;

    private String code;

    private String description;

    private Byte status;

    private String action;

    public static BaseRole parseRoleInstance(List<BaseRole> baseRoleList, Role role) {
        for (BaseRole baseRole : baseRoleList) {
            if (baseRole.getCode().equalsIgnoreCase(role.getCode())) {
                return baseRole;
            }
        }
        return null;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name == null ? null : name.trim();
    }

    public String getAlias() {
        return alias;
    }

    public void setAlias(String alias) {
        this.alias = alias == null ? null : alias.trim();
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code == null ? null : code.trim();
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description == null ? null : description.trim();
    }

    public Byte getStatus() {
        return status;
    }

    public void setStatus(Byte status) {
        this.status = status;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action == null ? null : action.trim();
    }
}
