package com.pamirs.tro.common.constant;

/**
 * @ClassName YNEnum
 * @Description
 * @Author qianshui
 * @Date 2020/4/27 上午11:20
 */
public enum YNEnum implements GeneralEnum {

    NO(0, "否"),
    YES(1, "是");

    private Integer value;

    private String desc;

    YNEnum(Integer value, String desc) {
        this.value = value;
        this.desc = desc;
    }

    @Override
    public Integer getValue() {
        return this.value;
    }

    @Override
    public String getDesc() {
        return this.desc;
    }
}
